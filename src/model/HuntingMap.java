	/**
	 * generates the map for this hunting instance by building terrain layouts randomly.
	 */
	private void generateMap(){
		
		//manually build map, until able (if ever) to manufacture algorithm
		String objName = "tree1";//change to rock when/if rock images made for collidable terrain
		//speed modification for moving through this terrain
		double objMoveMod;
		//chance shot is blocked each turn in this terrain
		double objStopShot;
		
		int mapWidth = (int) (this.MAP_WIDTH / 48.0);
		int mapHeight = (int) (this.MAP_HEIGHT / 48.0);
		
		//leave space in call for arguments to pass to generate map
		int[] altArgs = new int[1];
		altArgs[0]=0;
		
		for (int x = 0; x < mapWidth; x++){
			for (int y = 0; y < mapHeight; y++) {
				String nameExt = getExt(x,y,altArgs);
				if (nameExt.equals("90")){
					objMoveMod = 1;
					objStopShot = 0;
				}//nameExt not --
				else {
					objMoveMod = .6;
					objStopShot = .5;
				}//if based on name extension
				huntingGroundsMap.put(calcMapIdx(x,y), new TerrainObject(objName + nameExt, x,  y, this.bckGround, objMoveMod,  objStopShot));								
			}//for y
		}//for x
		
		
	}//generate map method

